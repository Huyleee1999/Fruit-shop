<script>

    Echo.join('chat')
        .here(function(users) {
            users.forEach(function(user) {
                const element = $('<li>').attr('id', user.id).text(user.fullname);
                $('#users').append(element);
            });
        })
        .joining(function(user) {
            const element = $('<li>').attr('id', user.id).text(user.fullname);
            $('#users').append(element);
        })
        .leaving(function(user) {
            const element = $('#' + user.id);
            if (element.length) {
                element.remove();
            }
        })
        .listen('MessageSent', function(e) {
            const element = $('<li>').text(e.user.fullname + ': ' + e.message);
            $('#messages').append(element);
            var $messageList = $('#messages'); 
            $messageList.scrollTop($messageList.prop('scrollHeight'));
        });

    $(document).on('click', '#send', function(e) {
        e.preventDefault();
        const messageElm = $('#message');
        window.axios.post('http://localhost:8000/chat/message', {
            message: messageElm.val(),
        })
        messageElm.val('');
    });

</script>